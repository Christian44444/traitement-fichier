
public class Ingredient {
	 private String libelle;

	/**
	 * Constructeur
	 * @param libelle
	 */
	public Ingredient(String libelle) {
		this.libelle = libelle;
	}

	/**
	 * @return the libelle
	 */
	public String getLibelle() {
		return libelle;
	}

	/**
	 * @param libelle the libelle to set
	 */
	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}
	
	
	 
	 
}
