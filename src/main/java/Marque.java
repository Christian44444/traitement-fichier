
public class Marque {
	public String nom;

	/**
	 * Constructeur
	 * @param nom
	 */
	public Marque(String nom) {
		this.nom = nom;
	}

	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}

	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	
	
	
}
